// 获取统计数据
export const getStatistics = () => {
  const countryStats = {};
  const cityStats = {};

  classmatesData.forEach(classmate => {
    // 统计国家
    countryStats[classmate.country] = (countryStats[classmate.country] || 0) + 1;
    // 统计城市
    cityStats[classmate.city] = (cityStats[classmate.city] || 0) + 1;
  });

  // 将统计结果转换为数组并排序
  const sortedCountryStats = Object.fromEntries(
    Object.entries(countryStats).sort(([,a], [,b]) => b - a)
  );

  const sortedCityStats = Object.fromEntries(
    Object.entries(cityStats).sort(([,a], [,b]) => b - a)
  );

  return {
    countryStats: sortedCountryStats,
    cityStats: sortedCityStats
  };
};